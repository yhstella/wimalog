#!/usr/bin/env node
// Supabase RPC를 빌드 타임에 호출해 src/lib/snapshot.generated.js 생성
// 결과: 첫 진입 시 0ms로 정적 통계 노출 → 사용자 입력 시 background에서 fresh RPC

import { createClient } from '@supabase/supabase-js';
import { config } from 'dotenv';
import { writeFileSync, existsSync, readFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

config({ path: '.env.local' });
config({ path: '.env' });

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = resolve(ROOT, 'src/lib/snapshot.generated.js');

const URL = process.env.SUPABASE_URL || process.env.VITE_SUPABASE_URL;
const KEY = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.SUPABASE_ANON_KEY || process.env.VITE_SUPABASE_ANON_KEY;

if (!URL || !KEY) {
  console.warn('[snapshot] Supabase 키 없음 — 기존 snapshot 유지 (있다면) 또는 빈 snapshot 작성');
  if (!existsSync(OUT)) writeEmpty();
  process.exit(0);
}

const supa = createClient(URL, KEY, { auth: { persistSession: false } });
const MEDS = ['wegovy', 'mounjaro', 'saxenda', 'ozempic', 'zepbound'];
const COMMON_WEEKS = [4, 8, 12, 16, 24, 36, 48];

async function rpc(name, params = {}) {
  try {
    const { data, error } = await supa.rpc(name, params);
    if (error) { console.warn(`  rpc ${name} err:`, error.message); return null; }
    return data;
  } catch (e) {
    console.warn(`  rpc ${name} threw:`, e.message);
    return null;
  }
}

function writeEmpty() {
  const body = `// Auto-generated by scripts/build-snapshot.mjs — DO NOT EDIT
// Supabase 미설정 또는 빌드 실패 시 빈 스냅샷
export const SNAPSHOT_AT = null;
export const SNAPSHOT = { empty: true };
`;
  mkdirSync(dirname(OUT), { recursive: true });
  writeFileSync(OUT, body, 'utf-8');
}

console.log('[snapshot] Supabase RPC 수집 중...');
const start = Date.now();

const [
  platformScale,
  globalCurve,
  topMedsRecent,
  exerciseStats,
  pharmacySummary,
  pharmaciesByRegion,
  ...perMedResults
] = await Promise.all([
  rpc('platform_scale'),
  rpc('avg_loss_curve', { med: null, gender_f: null, age_grp: null, bmi_min: null, bmi_max: null, weeks_arr: COMMON_WEEKS }),
  rpc('top_recent_medications', { days: 30 }),
  rpc('exercise_stats', { days: 30 }),
  rpc('pharmacy_summary'),
  rpc('pharmacies_by_region', { med: null }),
  ...MEDS.flatMap(med => [
    rpc('avg_loss_curve', { med, gender_f: null, age_grp: null, bmi_min: null, bmi_max: null, weeks_arr: COMMON_WEEKS }),
    rpc('side_effect_rates', { med }),
    rpc('price_stats', { med }),
    rpc('discontinuation_stats', { med }),
    rpc('rebound_curve', { med, weeks_arr: COMMON_WEEKS }),
    rpc('rebound_by_exercise', { med, target_week: 24, threshold_min: 90 }),
  ]),
]);

// 약별 결과 묶기
const byMed = {};
MEDS.forEach((med, i) => {
  const base = i * 6;
  byMed[med] = {
    curve: perMedResults[base],
    sideEffects: perMedResults[base + 1],
    prices: perMedResults[base + 2],
    discontinuation: perMedResults[base + 3],
    rebound: perMedResults[base + 4],
    reboundByExercise: perMedResults[base + 5],
  };
});

const snapshot = {
  platformScale,
  globalCurve,
  topMedsRecent,
  exerciseStats,
  pharmacySummary,
  pharmaciesByRegion,
  byMed,
};

const ms = Date.now() - start;
const sizeKb = (JSON.stringify(snapshot).length / 1024).toFixed(1);

// RPC 결과 검증 — 핵심 cohort 데이터(globalCurve + perMed.curve)가 다 null/빈 결과면
// snapshot.generated.js 를 빈 결과로 덮어쓰지 말고 git 의 옛 snapshot 그대로 유지.
// 이유: Vercel CI 에서 supabase RPC 가 일시적으로 실패하면 production 사이트의
// 시뮬레이터에 `curve:null` 박힌 빈 snapshot 이 푸시돼 첫 paint 가 비어보임.
// 옛 snapshot 이 stale 해도 cold-cache 의미있는 데이터를 보장하는 게 우선.
const hasGlobalCurve = Array.isArray(globalCurve) && globalCurve.length > 0;
const hasAnyMedCurve = MEDS.some((m) => Array.isArray(byMed[m]?.curve) && byMed[m].curve.length > 0);
if (!hasGlobalCurve && !hasAnyMedCurve) {
  console.warn(`[snapshot] 모든 cohort RPC 결과 null/빈 — 기존 ${OUT} 유지 (덮어쓰지 않음). ms=${ms} sizeKb=${sizeKb}`);
  if (!existsSync(OUT)) writeEmpty();
  process.exit(0);
}

console.log(`[snapshot] ${ms}ms · ${sizeKb}KB · hasGlobal=${hasGlobalCurve} hasAnyMed=${hasAnyMedCurve}`);

const body = `// Auto-generated by scripts/build-snapshot.mjs at ${new Date().toISOString()}
// 빌드 타임에 Supabase RPC를 미리 호출 → 첫 진입 시 정적 노출
// 사용자 입력 시 background에서 RPC로 fresh 데이터 교체
export const SNAPSHOT_AT = ${JSON.stringify(new Date().toISOString())};
export const SNAPSHOT = ${JSON.stringify(snapshot, null, 2)};
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, body, 'utf-8');
console.log(`[snapshot] → ${OUT}`);
